import java.util.*;
public class EX_3Arrays {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int arr[]  = new int[5];
        for (int i = 0; i < arr.length; i++) {
            arr[i] =  in.nextInt();
            System.out.println(arr[i]);
        }
     }
}